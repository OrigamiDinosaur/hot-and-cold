using System;
using UnityEngine;

namespace Apache.Core {

	[CreateAssetMenu(fileName = SCRIPTABLE_OBJECT_NAME, menuName = EVENT_CONTAINER_MENU_NAME_PREFIX + SCRIPTABLE_OBJECT_NAME, order = MENU_STARTING_ORDER)]
	public class EventContainer : EventContainerBase {
		
		//-----------------------------------------------------------------------------------------
		// Constants:
		//-----------------------------------------------------------------------------------------

		private const string SCRIPTABLE_OBJECT_NAME = "Event Container";

		//-----------------------------------------------------------------------------------------
		// Events:
		//-----------------------------------------------------------------------------------------

		public event Action Event;

		//-----------------------------------------------------------------------------------------
		// Public Methods:
		//-----------------------------------------------------------------------------------------

		public void Invoke() {
			Event?.Invoke();
		}
	}

	public class EventContainer<T> : EventContainerBase {

		//-----------------------------------------------------------------------------------------
		// Events:
		//-----------------------------------------------------------------------------------------

		public event Action<T> Event;

		//-----------------------------------------------------------------------------------------
		// Public Methods:
		//-----------------------------------------------------------------------------------------

		public virtual void Invoke(T value) {
			Event?.Invoke(value);
		}
	}

	public class EventContainer<T1, T2> : EventContainerBase {

		//-----------------------------------------------------------------------------------------
		// Events:
		//-----------------------------------------------------------------------------------------

		protected event Action<T1, T2> Event;

		//-----------------------------------------------------------------------------------------
		// Public Methods:
		//-----------------------------------------------------------------------------------------

		public virtual void Invoke(T1 val1, T2 val2) {
			Event?.Invoke(val1, val2);
		}
	}

	public class EventContainer<T1, T2, T3> : EventContainerBase {

		//-----------------------------------------------------------------------------------------
		// Events:
		//-----------------------------------------------------------------------------------------

		protected event Action<T1, T2, T3> Event;

		//-----------------------------------------------------------------------------------------
		// Public Methods:
		//-----------------------------------------------------------------------------------------

		public virtual void Invoke(T1 val1, T2 val2, T3 val3) {
			Event?.Invoke(val1, val2, val3);
		}
	}
}