using System.Linq;
using UnityEngine;

namespace Apache.Core {

	//-----------------------------------------------------------------------------------------
	// Enumerations:
	//-----------------------------------------------------------------------------------------

	public enum RefOrders {
		None,
		Name
	}

	//-----------------------------------------------------------------------------------------
	// Classes:
	//-----------------------------------------------------------------------------------------

	public abstract class OrderableRefAttribute : RefAttribute {

		//-----------------------------------------------------------------------------------------
		// Public Properties:
		//-----------------------------------------------------------------------------------------

		public RefOrders Order { get; set; }

		//-----------------------------------------------------------------------------------------
		// Protected Methods:
		//-----------------------------------------------------------------------------------------

		protected Object[] OrderObjects(Object[] objects) {
			switch (Order) {
				case RefOrders.Name:
					objects = objects.OrderBy(c => c.name).ToArray();
					break;
			}
			return objects;
		}
	}
}